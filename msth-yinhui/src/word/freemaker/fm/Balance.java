package word.freemaker.fm;


public class Balance {
	private String customer;//客户
	private String transAccountNo;//交易账号
	private String customerAccountNo;//客户账号
	private String website;//网点
	private String deadline;//报告截止日期
	private String proCode;//产品代码
	private String proName;//产品名称
	private String shareBalance;//份额余额
	private String unitWorth;//单位净值
	private String refMarketValue;//参考市值
	private String year;//年
	private String month;//月
	private String day;//日
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getTransAccountNo() {
		return transAccountNo;
	}
	public void setTransAccountNo(String transAccountNo) {
		this.transAccountNo = transAccountNo;
	}
	public String getCustomerAccountNo() {
		return customerAccountNo;
	}
	public void setCustomerAccountNo(String customerAccountNo) {
		this.customerAccountNo = customerAccountNo;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getDeadline() {
		return deadline;
	}
	public void setDeadline(String deadline) {
		this.deadline = deadline;
	}
	public String getProCode() {
		return proCode;
	}
	public void setProCode(String proCode) {
		this.proCode = proCode;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getUnitWorth() {
		return unitWorth;
	}
	public void setUnitWorth(String unitWorth) {
		this.unitWorth = unitWorth;
	}
	public String getRefMarketValue() {
		return refMarketValue;
	}
	public void setRefMarketValue(String refMarketValue) {
		this.refMarketValue = refMarketValue;
	}
	public String getShareBalance() {
		return shareBalance;
	}
	public void setShareBalance(String shareBalance) {
		this.shareBalance = shareBalance;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
}
